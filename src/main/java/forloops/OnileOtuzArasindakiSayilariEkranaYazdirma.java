package forloops;

public class OnileOtuzArasindakiSayilariEkranaYazdirma {
    public static void main(String[] args) {

        /*
         10 ile 30 arasindaki (10 ve 30 dahil) sayilari aralarinda virgul olarak ayni satirda yazdirin.
         */
        System.out.println();

        for (int i = 10; i < 31; i++) {
            System.out.print(i + ",");
        }

    }
}
