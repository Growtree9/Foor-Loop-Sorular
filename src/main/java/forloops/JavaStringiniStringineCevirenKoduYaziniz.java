package forloops;

public class JavaStringiniStringineCevirenKoduYaziniz {
    public static void main(String[] args) {

       // "Java" String'ini J*a*v*a String'ine ceviren kodu yaziniz

        String sum = "Java";

        for (int i = 0; i <sum.length() ; i++) {

            System.out.print(sum.charAt(i) + "*");

        }

    }
}
