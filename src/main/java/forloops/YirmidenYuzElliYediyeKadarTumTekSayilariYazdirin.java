package forloops;

public class YirmidenYuzElliYediyeKadarTumTekSayilariYazdirin {
    public static void main(String[] args) {

        // 28 den 157 ye kadar tum tek  sayilari ekrana yazdiran kodu yaziniz

        for (int i = 28; i <158 ; i++) {

            if (i%2!=0)
                System.out.print(i + " ");
        }
    }
}
